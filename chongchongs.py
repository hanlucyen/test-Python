from turtle import *
setup(550,600,330,10)
delay(0)
bgcolor('red')
t=Turtle()
t.begin_poly()
for i in range(4):
	t.lt(360/8)
	t.fd(60)
	t.lt(360/8)
	t.fd(70)
	t.goto(0,0)
t.end_poly()
t2=t.get_poly()
addshape('cquat',t2)
t.clear(),t.ht()
t3=t.clone()
t3.shape('square')
t3.shapesize(3,2,2)
t3.seth(270)
t3.pensize(15)
t3.fd(200)
t4.shape('cquat')
t4.fillcolor('#00e640')
delay(20)
while True:
    t4.circle(0)

done()
